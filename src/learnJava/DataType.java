package learnJava;

public class DataType {

	public static void main(String[] args) {

		//Primitive data type
		int x = 100;
		float num1 = 9.55f;
		short num2 = 2;
		long num3 = 111111111;	// there is a limit for long also
		double num4 = 10.50;
		boolean var = true;
		char letter = 'x';  //we use single cote for character
		
		// non primitive data type
		String word = ("Hello World");	// String always starts from capital S. and we need "" for this
		String word2 = ("This is my first java script");
		
		System.out.println(x);		// syso then press ctrl+space
		System.out.println(num1);	// here ln means next line
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(var);
		System.out.println(letter);
		System.out.println(word);
		System.out.println(word2);

	}

}
